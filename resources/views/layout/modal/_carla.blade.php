                <p class="text-justify">Publicitária por formação, a capixaba Carla Buaiz se encantou por trabalhos artesanais ainda na faculdade.
                    Fez cursos de escultura, ourivesaria e decidiu então mudar o rumo de sua carreira.
                    Depois de dois anos produzindo artesanalmente suas próprias joias, se especializou no assunto e investiu nesta paixão.</p>
                <br/>
                <p class="text-justify">Desde então, Carla não parou, sempre em busca de oferecer mais diversidade em suas criações.
                    Estudou dentro e fora do Brasil: ourivesaria, História da Moda e a Evolução do Estilo na University of the Arts London, ainda História & Inspirações e também Gemologia na École Van Cleef & Arpels, da tradicional joalheria Van Cleef, em Paris.</p>
                <br/>
                <p class="text-justify">Em 2009, a designer de joias inaugurou sua primeira loja homônima, em Vitória, ES.
                    Buscando inspirações em viagens pelo mundo ou até mesmo em detalhes do cotidiano, suas criações são reconhecidas por terem traços que unem o moderno ao clássico.
                    A designer utiliza materiais inusitados como couro e chifres, mesclados a pedras preciosas e ouro.
                    O estilo tradicional também está presente nas peças de suas coleções: Carla Buaiz não abre mão de manter traços antigos, como a técnica de cravação com carretilha. <p/>
                <br/>
                <p class="text-justify">A marca lança três grandes coleções e pequenas cápsulas todos os anos, garantindo novidades e peças únicas no mercado o tempo todo.<p/>
                <br/>
                <p class="text-justify">Carla Buaiz está presente em feiras e eventos por todo o mundo com suas joias.
                    No Brasil, foi convidada pelo Vitória Cine Vídeo por cinco anos para prestigiar os homenageados com uma joia. Participou também da Mostra Black, em São Paulo;
                    Casa Cor, Morar Mais e Joia Brasil, no Rio de Janeiro.
                    No mercado internacional, as jóias Carla Buaiz já foram destaque em exposições como a Fresh From Brasil, em Nova York, e a Oslo Fashion Week, na Noruega, onde  representou a região sudeste do Brasil. </p>
                <br/>
                <p class="text-justify">Além da flagship na Praia do Canto, as joias Carla Buaiz podem ser encontradas na Jota Design Joias, no Rio de Janeiro, além do e-commerce da marca.
                    Em 2015 a designer chega a São Paulo</p>
